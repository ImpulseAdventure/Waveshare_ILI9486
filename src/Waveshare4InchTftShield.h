template<class Baseclass>
void
Waveshare4InchTftShieldTemplate<Baseclass>::fillScreen(uint16_t color)
{
	startWrite();
	Waveshare4InchTftShieldImpl::writeFillRect2(0, 0, Baseclass::width(), Baseclass::height(), color);
	endWrite();
}

