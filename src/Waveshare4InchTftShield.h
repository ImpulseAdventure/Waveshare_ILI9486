template<class Baseclass>
void
Waveshare4InchTftShieldTemplate<Baseclass>::writeFillRect(int16_t x, int16_t y, int16_t w, int16_t h, uint16_t color)
{
	//  Negative widths, so swap left and right sides
	if (w < 0)
	{
		w = -w;
		x -= w;
	}

	//  Negative height, so swap top and bottom
	if (h < 0)
	{
		h = -h;
		y -= h;
	}

	// Left side offscreen, clip
	if (x < 0)
	{
		w += x;
		x = 0;
	}

	// Top offscreen, clip
	if (y < 0)
	{
		h += y;
		y = 0;
	}

	//  Rightside offscreen, clip
	if (x + w > Baseclass::width())
	{
		w = Baseclass::width() - x;
	}

	// bottom offscreen, clip
	if (y + h > Baseclass::height())
	{
		h = Baseclass::height() - y;
	}

	//  Entire width or entire height is offscreen
	if (w <= 0) return;
	if (h <= 0) return;

	// Now, 0 <= x <= x+w <= WIDTH
	// And, 0 <= y <= y+h <= HEIGHT
	Waveshare4InchTftShieldImpl::writeFillRect2(x, y, w, h, color);
}

template<class Baseclass>
void
Waveshare4InchTftShieldTemplate<Baseclass>::writeFastVLine(int16_t x, int16_t y, int16_t h, uint16_t color)
{
	writeFillRect(x, y, 1, h, color);
}

